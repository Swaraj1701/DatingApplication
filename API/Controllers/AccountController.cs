using API.Data;
using API.DTOs;
using API.Entities;
using API.Extensions;
using API.Interfaces;
using Microsoft.AspNetCore.Mvc;
using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Storage.ValueConversion.Internal;
using System.Security.Cryptography;
using System.Text;

namespace API.Controllers
{
    public class AccountController(AppDbContext context, ITokenService tokenService) : BaseApiController
    {
        [HttpPost("register")]
        public async Task<ActionResult<UserDto>> Register(RegisterDto dto)
        {
            if (await EmailExists(dto.Email)) return BadRequest("Email already exists");
            using var hmac = new HMACSHA512();
            var user = new AppUser
            { DisplayName = dto.DisplayName, Email = dto.Email, PasswordHash = hmac.ComputeHash(Encoding.UTF8.GetBytes(dto.Password)), PasswordSalt = hmac.Key };
            context.Users.Add(user);
            await context.SaveChangesAsync();
            return user.ToDto(tokenService);
        }

        [HttpPost("login")]
        public async Task<ActionResult<UserDto>> Login(LoginDto loginDto)
        {
            var user = await context.Users.SingleOrDefaultAsync(x=> x.Email == loginDto.Email);
            if (user == null) return Unauthorized("Invalid Email or Password.");

            using var hmac = new HMACSHA512(user.PasswordSalt);

            var computedHash = hmac.ComputeHash(Encoding.UTF8.GetBytes(loginDto.Password));

            for (var i = 0; i < computedHash.Length; i++) {
                if (computedHash[i] != user.PasswordHash[i]) return Unauthorized("Invalid Email or Password.");
            }
            return user.ToDto(tokenService);
        }

        #region Private Methods
        private async Task<bool> EmailExists(string email)
        {
            return await context.Users.AnyAsync(x=> x.Email.ToLower() == email.ToLower());
        }
        #endregion
    }


}
